#include <stdio.h>

int main() {
    int a, b, sum;

    // Input first number
    printf("Enter first number: ");
    scanf("%d", &a);

    // Input second number
    printf("Enter second number: ");
    scanf("%d", &b);

    // Addition
    sum = a + b;

    // Output result
    printf("Sum = %d", sum);

    return 0;
}
